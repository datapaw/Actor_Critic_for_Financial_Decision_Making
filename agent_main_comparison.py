def load_critic_dataset(critic_csv_path=None):
	"""
	Load sp500_critic.csv which contains:
	- Original market features (Close, High, Low, Open, Volume)
	- Ensemble voting outputs (ensemble_prediction, ensemble_confidence, vote_entropy, etc.)
	- Forward return columns (forward_return_10/15/30/60 and _1/_2 variants)
	- Technical indicators (RSI, MACD, Bollinger Bands, ATR, etc.)
	- Strategy metrics
	
	Returns:
		DataFrame with all critic data
	"""
	repo_root = Path(__file__).resolve().parent
	if critic_csv_path is None:
		critic_csv_path = repo_root / 'data' / 'dataset' / 'sp500_critic.csv'
	
	critic_csv_path = Path(critic_csv_path)
	if not critic_csv_path.exists():
		raise FileNotFoundError(f"Critic CSV not found at: {critic_csv_path}")
	
	df = pd.read_csv(critic_csv_path)
	
	# Parse date
	if 'Date' in df.columns:
		df['Date'] = pd.to_datetime(df['Date'], errors='coerce')
	
	print(f"Loaded critic dataset: {len(df)} rows, {len(df.columns)} columns")
	
	return df


def split_train_val_test_indices(n_samples, val_size=0.15, test_size=0.15):
	"""Split indices chronologically for time series."""
	test_idx = int(n_samples * (1 - test_size))
	val_idx = int(test_idx * (1 - val_size))
	
	train_indices = list(range(0, val_idx))
	val_indices = list(range(val_idx, test_idx))
	test_indices = list(range(test_idx, n_samples))
	
	return train_indices, val_indices, test_indices


def prepare_critic_features(df):
	"""
	Prepare feature matrix for critic model (matches agent_main_critic.py).
	
	Features include ONLY information available at decision time:
	1. Ensemble voting outputs (confidence, entropy, agreements)
	2. Market conditions (price, volume)
	3. Technical indicators (RSI, MACD, Bollinger Bands, ATR, momentum, etc.)
	4. Flags and reliability metrics
	
	EXCLUDED (future information):
	- forward_return_* (future returns)
	- strategy_class_* (derived from future returns)
	- strategy_confidence_* / strategy_margin_* (redundant)
	
	Total: ~36 features matching trained critic model
	"""
	feature_cols = []
	
	# 1. Ensemble voting outputs
	voting_cols = [
		'ensemble_prediction',
		'ensemble_confidence',
		'vote_entropy',
		'majority_percentage',
		'prediction_margin',
		'prediction_margin_normalized',
		'models_in_agreement',
		'agreement_percentage',
		'prob_class_0',
		'prob_class_1',
		'prob_class_2',
		'prob_class_3'
	]
	feature_cols.extend([c for c in voting_cols if c in df.columns])
	
	# 2. Market features
	market_cols = ['Close', 'High', 'Low', 'Open', 'Volume']
	feature_cols.extend([c for c in market_cols if c in df.columns])
	
	# 3. Technical indicators
	technical_cols = [
		'rsi_14', 'macd_line', 'macd_signal', 'macd_histogram',
		'bb_upper', 'bb_lower', 'bb_width', 'bb_position',
		'atr_14', 'momentum_10', 'sma_5', 'sma_20', 'ema_12',
		'price_position', 'volatility_20', 'volume_sma_20', 'volume_ratio'
	]
	feature_cols.extend([c for c in technical_cols if c in df.columns])
	
	# 4. Decision flags
	flag_cols = ['had_tie', 'low_confidence_flag']
	feature_cols.extend([c for c in flag_cols if c in df.columns])
	
	# Extract and prepare features
	X_df = df[feature_cols].copy()
	
	# Convert boolean to int
	for col in X_df.columns:
		if X_df[col].dtype == bool:
			X_df[col] = X_df[col].astype(int)
	
	# Handle missing values
	X_df = X_df.replace(["", " ", None], np.nan).fillna(0)
	for col in X_df.columns:
		X_df[col] = pd.to_numeric(X_df[col], errors='coerce').fillna(0)
	
	X = X_df.values.astype(np.float32)
	
	return X, feature_cols


def load_critic_model(models_dir: str | Path = None):
	"""Load trained critic model."""
	_require_tf()
	repo_root = Path(__file__).resolve().parent
	models_dir = Path(models_dir) if models_dir else (repo_root / 'models')
	critic_path = models_dir / 'critic_best.keras'
	
	if not critic_path.exists():
		print(f"Warning: critic model not found at {critic_path}")
		return None
	
	try:
		critic = keras.models.load_model(critic_path)
		print(f"Loaded critic model from {critic_path}")
		return critic
	except Exception as e:
		print(f"Error loading critic model: {e}")
		return None


def run_critic_inference(critic_model, X_test):
	"""
	Run critic inference to get timing decisions.
	
	Returns:
		Dict with timing decisions and probabilities
	"""
	if critic_model is None:
		print("Critic model not available")
		return None
	
	try:
		timing_probs = critic_model.predict(X_test, verbose=0)  # (n_samples, 3)
		timing_preds = np.argmax(timing_probs, axis=1)
		timing_confs = np.max(timing_probs, axis=1)
		
		# Timing classes: 0=immediate, 1=wait_2_days
		timing_names = {0: 'immediate', 1: 'wait_2_days'}
		timing_pred_names = np.array([timing_names[t] for t in timing_preds])
		
		return {
			'timing_predictions': timing_preds,
			'timing_confidences': timing_confs,
			'timing_probabilities': timing_probs,
			'timing_names': timing_pred_names,
		}
	except Exception as e:
		print(f"Error running critic inference: {e}")
		return None


def extract_actual_returns_from_critic_csv(df_test):
	"""
	Extract actual returns from forward_return columns in sp500_critic.csv.
	
	The critic CSV already has forward_return_10, forward_return_15, forward_return_30, forward_return_60
	which represent actual returns at those horizons.
	
	Returns:
		Dict with arrays of actual returns for each horizon
	"""
	returns = {}
	
	for period in [10, 15, 30, 60]:
		col_name = f'forward_return_{period}'
		if col_name in df_test.columns:
			returns[period] = df_test[col_name].values.astype(np.float32)
		else:
			print(f"Warning: Column {col_name} not found in critic CSV")
			returns[period] = np.full(len(df_test), np.nan, dtype=np.float32)
	
	return returns


def generate_comparison_report(df_test, timing_results, actual_returns):
	"""
	Generate detailed comparison report between voting, critic, and actual returns.
	
	Args:
		df_test: Test set DataFrame from sp500_critic.csv
		timing_results: Dict with critic timing predictions
		actual_returns: Dict with actual returns {10: array, 15: array, 30: array, 60: array}
	
	Returns:
		DataFrame with comparison results
	"""
	period_names = {0: '60-day', 1: '30-day', 2: '15-day', 3: '10-day'}
	period_to_days = {0: 60, 1: 30, 2: 15, 3: 10}
	timing_names = {0: 'immediate', 1: 'wait_2_days'}
	
	n_samples = len(df_test)
	
	# Extract ensemble voting outputs
	ensemble_preds = df_test['ensemble_prediction'].values
	ensemble_confs = df_test['ensemble_confidence'].values
	
	# Map voting prediction to actual return at that horizon
	predicted_returns = np.zeros(n_samples, dtype=np.float32)
	predicted_horizons = []
	
	for i in range(n_samples):
		pred_class = int(ensemble_preds[i])
		horizon_days = period_to_days.get(pred_class, 30)
		predicted_horizons.append(period_names[pred_class])
		predicted_returns[i] = actual_returns[horizon_days][i]
	
	# Create comparison dataframe
	comparison_df = pd.DataFrame({
		'date': df_test['Date'].values if 'Date' in df_test.columns else range(n_samples),
		'voting_prediction': predicted_horizons,
		'voting_confidence': ensemble_confs,
		'voting_class': ensemble_preds,
		'prob_60day': df_test['prob_class_0'].values if 'prob_class_0' in df_test.columns else np.nan,
		'prob_30day': df_test['prob_class_1'].values if 'prob_class_1' in df_test.columns else np.nan,
		'prob_15day': df_test['prob_class_2'].values if 'prob_class_2' in df_test.columns else np.nan,
		'prob_10day': df_test['prob_class_3'].values if 'prob_class_3' in df_test.columns else np.nan,
		'actual_return_60day': actual_returns[60],
		'actual_return_30day': actual_returns[30],
		'actual_return_15day': actual_returns[15],
		'actual_return_10day': actual_returns[10],
		'predicted_return': predicted_returns,
	})
	
	# Add critic results if available
	if timing_results:
		timing_preds = timing_results['timing_predictions']
		timing_confs = timing_results['timing_confidences']
		
		comparison_df['timing_decision'] = [timing_names[t] for t in timing_preds]
		comparison_df['timing_confidence'] = timing_confs
		comparison_df['timing_class'] = timing_preds
	
	return comparison_df


def generate_summary_statistics(comparison_df, timing_results):
	"""
	Generate summary statistics for the comparison report.
	
	Returns:
		Dict with summary statistics
	"""
	summary = {}
	
	# Period mapping
	period_names = {0: '60-day', 1: '30-day', 2: '15-day', 3: '10-day'}
	period_to_days = {0: 60, 1: 30, 2: 15, 3: 10}
	return_col_map = {60: 'actual_return_60day', 30: 'actual_return_30day', 15: 'actual_return_15day', 10: 'actual_return_10day'}
	
	# ====================================================================
	# CUMULATIVE RETURNS BY PERIOD (Actual vs Predicted)
	# ====================================================================
	cumulative_stats = {}
	
	for period_days, period_label in [(10, '10-day'), (15, '15-day'), (30, '30-day'), (60, '60-day')]:
		# Actual returns for this period
		actual_col = return_col_map[period_days]
		actual_rets = comparison_df[actual_col].values
		actual_rets = actual_rets[~np.isnan(actual_rets)]
		actual_sum = np.sum(actual_rets)
		
		# Predicted returns: sum returns where this period was predicted
		period_key = [k for k, v in period_to_days.items() if v == period_days][0]
		mask = comparison_df['voting_class'] == period_key
		predicted_rets = comparison_df.loc[mask, actual_col].values
		predicted_rets = predicted_rets[~np.isnan(predicted_rets)]
		predicted_sum = np.sum(predicted_rets)
		
		cumulative_stats[period_label] = {
			'actual_sum': float(actual_sum),
			'predicted_sum': float(predicted_sum),
			'actual_count': int(len(actual_rets)),
			'predicted_count': int(len(predicted_rets)),
			'predicted_vs_max': 0.0  # Will be calculated after
		}
	
	# Calculate max actual sum across all periods
	max_actual = max([v['actual_sum'] for v in cumulative_stats.values()]) if cumulative_stats else 1.0
	if max_actual == 0:
		max_actual = 1.0  # Avoid division by zero
	
	# Calculate ratios (predicted / max_actual)
	for period_label in cumulative_stats:
		cumulative_stats[period_label]['predicted_vs_max'] = cumulative_stats[period_label]['predicted_sum'] / max_actual
	
	summary['cumulative_returns'] = cumulative_stats
	
	# ====================================================================
	# VOTING PREDICTIONS ANALYSIS
	# ====================================================================
	voting_stats = {}
	
	for voting_class, period_name in period_names.items():
		mask = comparison_df['voting_class'] == voting_class
		if mask.sum() > 0:
			returns = comparison_df.loc[mask, 'predicted_return'].values
			valid_rets = returns[~np.isnan(returns)]
			
			if len(valid_rets) > 0:
				voting_stats[period_name] = {
					'samples': int(mask.sum()),
					'mean_return': float(np.mean(valid_rets)),
					'median_return': float(np.median(valid_rets)),
					'std_return': float(np.std(valid_rets)),
					'win_rate': float(np.sum(valid_rets > 0) / len(valid_rets) * 100),
					'best': float(np.max(valid_rets)),
					'worst': float(np.min(valid_rets))
				}
	
	summary['voting_stats'] = voting_stats
	
	# Critic timing analysis
	if timing_results and 'timing_class' in comparison_df.columns:
		timing_names = {0: 'immediate', 1: 'wait_2_days'}
		timing_stats = {}
		
		for timing_id, timing_name in timing_names.items():
			mask = comparison_df['timing_class'] == timing_id
			if mask.sum() > 0:
				returns = comparison_df.loc[mask, 'predicted_return'].values
				valid_rets = returns[~np.isnan(returns)]
				
				if len(valid_rets) > 0:
					timing_stats[timing_name] = {
						'samples': int(mask.sum()),
						'mean_return': float(np.mean(valid_rets)),
						'median_return': float(np.median(valid_rets)),
						'std_return': float(np.std(valid_rets)),
						'win_rate': float(np.sum(valid_rets > 0) / len(valid_rets) * 100),
						'best': float(np.max(valid_rets)),
						'worst': float(np.min(valid_rets))
					}
		
		summary['timing_stats'] = timing_stats
		
		# Correlation between critic confidence and returns
		valid_mask = ~np.isnan(comparison_df['predicted_return'])
		if valid_mask.sum() > 1:
			corr = np.corrcoef(
				comparison_df.loc[valid_mask, 'timing_confidence'],
				comparison_df.loc[valid_mask, 'predicted_return']
			)[0, 1]
			summary['confidence_return_correlation'] = float(corr)
	
	# Best horizon analysis
	horizon_stats = {}
	for col, horizon_name in [('actual_return_60day', '60-day'), 
	                           ('actual_return_30day', '30-day'),
	                           ('actual_return_15day', '15-day'),
	                           ('actual_return_10day', '10-day')]:
		if col in comparison_df.columns:
			vals = comparison_df[col].values
			valid = vals[~np.isnan(vals)]
			if len(valid) > 0:
				horizon_stats[horizon_name] = {
					'mean': float(np.mean(valid)),
					'win_rate': float(np.sum(valid > 0) / len(valid) * 100)
				}
	
	summary['horizon_stats'] = horizon_stats
	
	return summary



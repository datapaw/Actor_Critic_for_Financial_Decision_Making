"""
Comparison: Ensemble Voting vs Critic Model vs Actual Returns

This script analyzes the complete trading decision pipeline:
1. Load sp500_critic.csv (contains ensemble voting outputs + market data + technical indicators)
2. Extract test set using chronological split
3. Run critic model on voting outputs → timing decisions (immediate/wait_2_days)
4. Calculate actual returns at different horizons from forward_return columns
5. Compare:
   - What voting predicted (60/30/15/10 day horizons)
   - What critic recommended (immediate/wait 1/wait 2 days)
   - What actually happened (actual returns)

Input: sp500_critic.csv (generated by agent_main_loop.py)
Output: voting_vs_critic_comparison_now_or_2.csv with detailed analysis
"""


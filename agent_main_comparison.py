def main():
	"""
	Main comparison pipeline:
	1. Load sp500_critic.csv (has ensemble voting + technical indicators + forward returns)
	2. Extract test set
	3. Prepare critic features and run critic model
	4. Compare voting predictions vs critic timing vs actual returns
	5. Generate detailed analysis report
	"""
	print("="*80)
	print("ENSEMBLE VOTING vs CRITIC vs ACTUAL RETURNS COMPARISON")
	print("="*80 + "\n")
	
	# ========================================================================
	# STEP 1: Load Critic Dataset
	# ========================================================================
	print("STEP 1: Loading critic dataset (sp500_critic.csv)...")
	df_full = load_critic_dataset()
	print(f"  Total samples: {len(df_full)}")
	
	# Check for required columns
	required_cols = ['ensemble_prediction', 'ensemble_confidence', 'Close']
	missing = [c for c in required_cols if c not in df_full.columns]
	if missing:
		print(f"ERROR: Missing required columns: {missing}")
		print("Please run agent_main_loop.py first to generate sp500_critic.csv")
		return
	
	print()
	
	# ========================================================================
	# STEP 2: Split Data into Train/Val/Test
	# ========================================================================
	print("STEP 2: Splitting data chronologically...")
	train_idx, val_idx, test_idx = split_train_val_test_indices(len(df_full))
	
	df_test = df_full.iloc[test_idx].reset_index(drop=True)
	
	print(f"  Train: {len(train_idx)} samples")
	print(f"  Val: {len(val_idx)} samples")
	print(f"  Test: {len(test_idx)} samples")
	
	if 'Date' in df_test.columns:
		print(f"  Test period: {df_test['Date'].iloc[0]} to {df_test['Date'].iloc[-1]}")
	print()
	
	# ========================================================================
	# STEP 3: Prepare Features and Load Critic Model
	# ========================================================================
	print("STEP 3: Preparing critic features and loading model...")
	X_full, feature_cols = prepare_critic_features(df_full)
	print(f"  Feature matrix shape: {X_full.shape}")
	print(f"  Number of features: {len(feature_cols)}")
	
	# Extract test set features
	X_test = X_full[test_idx]
	
	# Normalize (using StandardScaler like in agent_main_critic.py)
	print("  Normalizing features with StandardScaler...")
	scaler = StandardScaler()
	# Fit on train, transform test
	X_train = X_full[train_idx]
	scaler.fit(X_train)
	X_test_scaled = scaler.transform(X_test)
	
	# Load critic model
	critic_model = load_critic_model()
	print()
	
	# ========================================================================
	# STEP 4: Run Critic Inference
	# ========================================================================
	if critic_model is None:
		print("STEP 4: Skipping critic inference (model not available)\n")
		timing_results = None
	else:
		print("STEP 4: Running critic inference...")
		timing_results = run_critic_inference(critic_model, X_test_scaled)
		
		if timing_results:
			timing_preds = timing_results['timing_predictions']
			timing_confs = timing_results['timing_confidences']
			timing_names_map = {0: 'immediate', 1: 'wait_2_days'}
			timing_counts = {t: np.sum(timing_preds == i) for i, t in timing_names_map.items()}
			
			print(f"  Critic results:")
			print(f"    Mean confidence: {np.mean(timing_confs):.4f}")
			for timing, count in timing_counts.items():
				print(f"    {timing}: {count} samples ({100*count/len(X_test):.1f}%)")
		print()
	
	# ========================================================================
	# STEP 5: Extract Actual Returns from Critic CSV
	# ========================================================================
	print("STEP 5: Extracting actual returns from forward_return columns...")
	actual_returns = extract_actual_returns_from_critic_csv(df_test)
	
	for period in [60, 30, 15, 10]:
		rets = actual_returns[period]
		valid = rets[~np.isnan(rets)]
		if len(valid) > 0:
			print(f"  {period}-day: mean={np.mean(valid):+.4f}, std={np.std(valid):.4f}, samples={len(valid)}")
	print()
	
	# ========================================================================
	# STEP 6: Generate Comparison Report
	# ========================================================================
	print("STEP 6: Generating comparison report...")
	comparison_df = generate_comparison_report(df_test, timing_results, actual_returns)
	
	# Save results
	results_dir = Path(__file__).resolve().parent / 'results'
	results_dir.mkdir(parents=True, exist_ok=True)
	
	# Save CSV
	comparison_csv = results_dir / 'voting_vs_critic_comparison_now_or_2.csv'
	comparison_df.to_csv(comparison_csv, index=False)
	print(f"  ✓ Saved comparison data to {comparison_csv}")
	
	# ========================================================================
	# STEP 7: Generate and Print Summary Statistics
	# ========================================================================
	print("\nSTEP 7: Generating summary statistics...")
	summary_stats = print_comparison_summary(comparison_df, timing_results)
	
	# Save detailed text report
	print("\nSTEP 8: Saving detailed report...")
	comparison_txt = results_dir / 'voting_vs_critic_comparison_report_now_or_2.txt'
	save_comparison_report(comparison_df, timing_results, summary_stats, output_path=comparison_txt)
	
	# Analyze and save timing breakdown by period
	if timing_results:
		print("\nSTEP 9: Analyzing timing decisions by period...")
		timing_analysis = analyze_timing_by_period(comparison_df)
		if timing_analysis:
			print_timing_by_period(timing_analysis)
			timing_txt = results_dir / 'timing_by_period_now_or_2.txt'
			save_timing_by_period(timing_analysis, output_path=timing_txt)
	
	print("\n" + "="*80)
	print("FILES SAVED:")
	print("="*80)
	print(f"  CSV (detailed data):  {comparison_csv}")
	print(f"  TXT (summary report): {comparison_txt}")
	if timing_results:
		print(f"  TXT (timing analysis): {timing_txt}")
	print("="*80)


if __name__ == '__main__':
	main()
